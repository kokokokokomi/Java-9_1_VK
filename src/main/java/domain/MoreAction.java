package domain;

public class MoreAction {

    private int addToFavorites;
    private int subscribeForComments;
    private int complain;

    public int getAddToFavorites() {
        return addToFavorites;
    }
    public void setAddToFavorites(int addToFavorites) {
        this.addToFavorites = addToFavorites;
    }

    public int getSubscribeForComments() {
        return subscribeForComments;
    }
    public void setSubscribeForComments(int subscribeForComments) {
        this.subscribeForComments = subscribeForComments;
    }

    public int getComplain() {
        return complain;
    }
    public void setComplain(int complain) {
        this.complain = complain;
    }
}
